;============================================================
; 实验六：8255A 并行接口控制LED实验
; 功能：A口输出驱动4个LED顺序显示0~F（每个状态1秒）
;       电平开关（B口最低位）：
;         低电平 → LED顺序显示
;         扳到高电平 → LED熄灭
;         再扳回低电平 → 程序结束
; 运行环境：8086 + 8255A（A口地址280H, B口281H, C口282H, 控制口283H）
;============================================================

CODE SEGMENT
    ASSUME CS:CODE, DS:CODE
    ORG 100H

START:
    JMP INIT

; ---- 数据区 ----
COUNT   DB 0                ; 当前显示值 (0~0FH)

; ---- 初始化 8255A ----
INIT:
    MOV DX, 283H            ; 8255A 控制口地址
    MOV AL, 82H             ; 10000010B: A口方式0输出，B口方式0输入，C口方式0输出
    OUT DX, AL

    MOV BL, 0               ; BL = 当前显示数值，从0开始

;============================================================
; 主循环：A口输出当前值 → 延时1秒 → 读B口开关 → 判断
;============================================================
MAIN_LOOP:
    ; ---- 2.1 输出当前值到LED ----
    MOV AL, BL
    MOV DX, 280H            ; A口地址
    OUT DX, AL              ; LED显示

    ; ---- 2.2 延时约1秒 ----
    CALL DELAY_1S

    ; ---- 2.3 读取开关状态 ----
    MOV DX, 281H            ; B口地址
    IN  AL, DX              ; 读入开关
    AND AL, 01H             ; 只保留最低位
    CMP AL, 01H             ; 开关 == 高电平 ?
    JE  LED_OFF             ; 是 → 跳转到熄灭处理

    ; ---- 开关为低电平，继续循环 ----
    INC BL                  ; 数值 +1
    AND BL, 0FH             ; 限制在 0~0FH 范围（高4位清零）
    JMP MAIN_LOOP           ; 继续显示下一个值

;============================================================
; LED熄灭处理：开关扳到高电平时进入
;============================================================
LED_OFF:
    ; ---- 熄灭所有LED ----
    MOV DX, 280H
    MOV AL, 00H
    OUT DX, AL              ; A口输出全0，LED全灭

; ---- 循环等待开关扳回低电平 ----
WAIT_LOW:
    MOV DX, 281H
    IN  AL, DX
    AND AL, 01H
    CMP AL, 00H             ; 开关 == 低电平 ?
    JE  EXIT_PROG           ; 是 → 退出程序
    JMP WAIT_LOW            ; 否 → 继续等待

;============================================================
; 程序出口：返回 DOS
;============================================================
EXIT_PROG:
    MOV AH, 4CH
    INT 21H

;============================================================
; 延时约1秒子程序
; 基于 4.77MHz 8088，每次 dec+jnz ≈ 5时钟周期
; 外层 8000H (32768) × 内层 FFFFH (65535) 根据实际时钟调整
;============================================================
DELAY_1S PROC
    PUSH CX
    PUSH DX
    MOV  CX, 8000H          ; 外层循环计数（可调整此值校准延时）

DELAY_OUTER:
    MOV  DX, 0FFFFH         ; 内层循环计数

DELAY_INNER:
    DEC  DX
    JNZ  DELAY_INNER
    DEC  CX
    JNZ  DELAY_OUTER

    POP  DX
    POP  CX
    RET
DELAY_1S ENDP

CODE ENDS
    END START
